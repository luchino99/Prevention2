#!/usr/bin/env node
/**
 * check-coverage-thresholds.mjs
 * ---------------------------------------------------------------------------
 * Sprint 6 task 6.1 (closes L-07). Per-module code coverage gate.
 *
 * Why per-module instead of vitest's built-in global thresholds
 * -------------------------------------------------------------
 * Vitest can enforce a single repo-wide threshold (e.g. "lines ≥ 80%"),
 * but a clinical engine doesn't have uniform criticality: SCORE2 needs
 * higher coverage than a logging helper. This gate enforces a
 * **per-module** floor so the validated-score engines stay tight while
 * less-critical infrastructure code can have a more permissive bar.
 *
 * Inputs
 * ------
 *   `coverage/coverage-summary.json` — generated by
 *   `npm run test:coverage` (V8 provider configured in
 *   `tests/vitest.config.ts`).
 *
 * Behaviour
 * ---------
 *   exit 0  → every tracked module meets its threshold (or no thresholds
 *             declare it — the gate is enforce-by-allowlist, not deny-all)
 *   exit 2  → at least one tracked module is below threshold
 *
 * Skip
 * ----
 *   If the summary file is missing (developer ran plain `npm test`, not
 *   `test:coverage`), the gate SKIPS rather than fails. Same pattern as
 *   `run-rls-tests.mjs` and `check-rls-coverage.mjs` — a CI step that
 *   doesn't have its prerequisite shouldn't block the build.
 *
 * Wired into
 * ----------
 *   `npm run check:coverage` — runs `test:coverage` then this gate
 *
 *   NOT wired into `build:check` directly. Coverage takes longer than
 *   the structural gates, and we want `build:check` to stay sub-30-sec.
 *   Run it as a separate CI step (or call `npm run check:coverage`
 *   from the GitHub Actions workflow alongside `npm test`).
 *
 * Updating thresholds
 * -------------------
 *   When a module legitimately drops in coverage (e.g. defensive code
 *   added that isn't exercised by tests), bump the floor down here and
 *   document why in the PR body. Otherwise add tests and keep the
 *   floor where it is. The point is to make coverage drops a CONSCIOUS
 *   choice, not silent decay.
 */

import { existsSync, readFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = resolve(__dirname, '..');
const SUMMARY_PATH = join(REPO_ROOT, 'coverage', 'coverage-summary.json');

const TAG = '[check-coverage-thresholds]';

/**
 * Per-module floor (% line coverage). Paths are repo-relative,
 * matching the keys vitest emits in `coverage-summary.json`.
 *
 * Tier 1 — validated clinical formulas (the highest bar):
 *   Each score module sits at the deep end of the dependency graph
 *   for clinical decisions. A regression here changes patient-facing
 *   numbers; coverage is the primary defence.
 *
 * Tier 2 — interpretation layers above validated scores:
 *   Composite risk aggregation, alert deriver, follow-up plan
 *   generator. High coverage required because Sprint 4 reorganised
 *   their contracts; regressions are easy to introduce.
 *
 * Tier 3 — supporting / infrastructure:
 *   Mid-tier bar to keep the engine pure code paths exercised
 *   without forcing exhaustive coverage of error handlers.
 *
 * Adjust thresholds in this object — never inside the gate logic.
 */
const THRESHOLDS = {
  // ── Tier 1 — validated formulas (90% line) ─────────────────────────
  'backend/src/domain/clinical/score-engine/score2.ts':                90,
  'backend/src/domain/clinical/score-engine/score2-diabetes.ts':       90,
  'backend/src/domain/clinical/score-engine/score2-eligibility.ts':    85,
  'backend/src/domain/clinical/score-engine/egfr.ts':                  90,
  'backend/src/domain/clinical/score-engine/fib4.ts':                  90,
  'backend/src/domain/clinical/score-engine/fli.ts':                   85,
  'backend/src/domain/clinical/score-engine/frail.ts':                 90,
  'backend/src/domain/clinical/score-engine/ada.ts':                   90,
  'backend/src/domain/clinical/score-engine/bmi.ts':                   90,
  'backend/src/domain/clinical/score-engine/metabolic-syndrome.ts':    85,

  // ── Tier 2 — interpretation layers (80% line) ──────────────────────
  'backend/src/domain/clinical/risk-aggregation/composite-risk.ts':    80,
  'backend/src/domain/clinical/alert-engine/alert-deriver.ts':         80,
  'backend/src/domain/clinical/followup-engine/followup-plan.ts':      80,

  // ── Tier 3 — supporting modules (70% line) ─────────────────────────
  'backend/src/domain/clinical/guideline-catalog/guideline-registry.ts': 70,
  'backend/src/domain/clinical/guideline-catalog/guideline-resolver.ts': 70,
};

/**
 * Resolve the V8 coverage summary's stable per-file key. Vitest writes
 * either an absolute path (most common) or a repo-relative path. We
 * normalise both to repo-relative.
 */
function normaliseKey(key) {
  // Strip any leading repo path prefix.
  if (key.startsWith(REPO_ROOT)) {
    return key.slice(REPO_ROOT.length).replace(/^\/+/, '');
  }
  return key.replace(/^\.\//, '');
}

if (!existsSync(SUMMARY_PATH)) {
  console.log(`${TAG} SKIP coverage/coverage-summary.json not found.`);
  console.log(`${TAG} Run \`npm run test:coverage\` first; or just \`npm run check:coverage\` for both.`);
  process.exit(0);
}

let summary;
try {
  summary = JSON.parse(readFileSync(SUMMARY_PATH, 'utf8'));
} catch (e) {
  console.error(`${TAG} FAIL coverage-summary.json is not valid JSON: ${e instanceof Error ? e.message : e}`);
  process.exit(2);
}

// V8 reporter shape:
//   {
//     "total": { "lines": {pct, ...}, "branches": ..., "functions": ..., "statements": ... },
//     "/abs/path/to/file.ts": { "lines": ..., ... },
//     ...
//   }
// We iterate everything except `total`.

const fileCoverage = new Map();
for (const [key, value] of Object.entries(summary)) {
  if (key === 'total') continue;
  if (!value || typeof value !== 'object') continue;
  const linesPct = value?.lines?.pct;
  if (typeof linesPct !== 'number') continue;
  fileCoverage.set(normaliseKey(key), linesPct);
}

const offenders = [];
const passed = [];
const missing = [];

for (const [path, threshold] of Object.entries(THRESHOLDS)) {
  const pct = fileCoverage.get(path);
  if (typeof pct !== 'number') {
    missing.push(path);
    continue;
  }
  if (pct < threshold) {
    offenders.push({ path, threshold, actual: pct });
  } else {
    passed.push({ path, threshold, actual: pct });
  }
}

if (missing.length > 0) {
  console.error(`${TAG} FAIL ${missing.length} module(s) tracked in THRESHOLDS but absent from coverage report:`);
  for (const m of missing) console.error(`${TAG}   - ${m}`);
  console.error(`${TAG} The file may have moved/been renamed. Update THRESHOLDS in this script.`);
  process.exit(2);
}

if (offenders.length > 0) {
  console.error(`${TAG} FAIL — ${offenders.length} module(s) below their coverage floor.`);
  for (const o of offenders) {
    console.error(
      `${TAG}   ${o.path}  ${o.actual.toFixed(1)}% < ${o.threshold}% (delta ${(o.actual - o.threshold).toFixed(1)} pp)`,
    );
  }
  console.error(`${TAG} Add tests OR (consciously) lower the floor in scripts/check-coverage-thresholds.mjs`);
  console.error(`${TAG} with a PR-body explanation. Don't paper over.`);
  process.exit(2);
}

console.log(`${TAG} OK — ${passed.length} module(s) at or above their coverage floor.`);
for (const p of passed.sort((a, b) => a.path.localeCompare(b.path))) {
  const delta = (p.actual - p.threshold).toFixed(1);
  console.log(
    `${TAG}   ${p.path.padEnd(60)} ${p.actual.toFixed(1).padStart(5)}% ≥ ${String(p.threshold).padStart(2)}%  (+${delta} pp)`,
  );
}
